/*
    For storing larger data types (like videos and pictures) MySQL supports:
		(BLOB data type: Binary Large Objects)
	1) TINYBLOB  : at most 255 bytes     (B)
    2) BLOB      : at most 65 kilo-bytes (KB)
    3) MEDIUMBLOB: at most 16 mega-bytes (MB)
    4) LONGBLOB  : at most 4 giga-bytes  (GB)
    
    And for storing text and text files: (storing characters)
    5) TINYTEXT: at most 255 bytes       (B)
    2) TEXT      : at most 65 kilo-bytes (KB)
    3) MEDIUMTEXT: at most 16 mega-bytes (MB)
    4) LONGTEXT  : at most 4 giga-bytes  (GB)
*/